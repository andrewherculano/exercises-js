//Função tradicional
const double = function (number) {
    return number * 2
}

const resultDouble = double(5)
//console.log(`O Dobro é: ${resultDouble}`)

//Arrow Function
const arrowDouble = (number) => {
    return number * 2
}

const resultArrowDouble = arrowDouble(10)
console.log(`O dobro da ArrowDouble é: ${resultArrowDouble}`)